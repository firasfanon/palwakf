create or replace function public.rpc_core_admin_user_profile_update_v1(
  p_target_user_id uuid,
  p_patch jsonb default '{}'::jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, core, platform, auth
as $$
declare
  v_ctx jsonb;
  v_actor uuid;
  v_is_super boolean;
  v_is_platform_admin boolean;
  v_allowed text[] := array['email','name','username','role','department','governorate','phone','avatar_url','unit_id','is_active','is_superuser','correlation_id','request_id','operation'];
  v_self_allowed text[] := array['name','department','phone','avatar_url','correlation_id','request_id','operation'];
  v_denied text[];
  v_result jsonb;
begin
  if p_target_user_id is null then
    raise exception 'PWF_TARGET_USER_REQUIRED' using errcode = 'P0001';
  end if;
  p_patch := coalesce(p_patch, '{}'::jsonb);
  v_ctx := platform._pwf_owner_write_actor_context_v1();
  v_actor := (v_ctx->>'actor_user_id')::uuid;
  v_is_super := coalesce((v_ctx->>'is_superuser')::boolean,false);
  v_is_platform_admin := coalesce((v_ctx->>'is_platform_admin')::boolean,false);

  select array_agg(key) into v_denied
  from jsonb_object_keys(p_patch) as key
  where not (key = any(case when p_target_user_id = v_actor and not (v_is_super or v_is_platform_admin) then v_self_allowed else v_allowed end));
  if v_denied is not null then
    raise exception 'PWF_FORBIDDEN_PROFILE_PATCH_KEYS %', v_denied using errcode = 'P0001';
  end if;

  if p_target_user_id <> v_actor and not (v_is_super or v_is_platform_admin) then
    raise exception 'PWF_ADMIN_PROFILE_UPDATE_FORBIDDEN' using errcode = 'P0001';
  end if;
  if p_target_user_id = v_actor and ((p_patch ? 'is_active') or (p_patch ? 'is_superuser') or (p_patch ? 'role')) then
    raise exception 'PWF_SELF_LOCKOUT_OR_SELF_PRIVILEGE_CHANGE_DENIED' using errcode = 'P0001';
  end if;
  if (p_patch ? 'is_superuser') and (p_patch->>'is_superuser')::boolean = true and not v_is_super then
    raise exception 'PWF_SUPERUSER_GRANT_REQUIRES_SUPERUSER' using errcode = 'P0001';
  end if;

  update core.admin_users as a
  set email = case when p_patch ? 'email' then nullif(p_patch->>'email','') else a.email end,
      name = case when p_patch ? 'name' then nullif(p_patch->>'name','') else a.name end,
      username = case when p_patch ? 'username' then nullif(p_patch->>'username','') else a.username end,
      role = case when p_patch ? 'role' then nullif(p_patch->>'role','') else a.role end,
      department = case when p_patch ? 'department' then nullif(p_patch->>'department','') else a.department end,
      governorate = case when p_patch ? 'governorate' then nullif(p_patch->>'governorate','') else a.governorate end,
      phone = case when p_patch ? 'phone' then nullif(p_patch->>'phone','') else a.phone end,
      avatar_url = case when p_patch ? 'avatar_url' then nullif(p_patch->>'avatar_url','') else a.avatar_url end,
      unit_id = case when p_patch ? 'unit_id' then nullif(p_patch->>'unit_id','')::uuid else a.unit_id end,
      is_active = case when p_patch ? 'is_active' then (p_patch->>'is_active')::boolean else a.is_active end,
      is_superuser = case when p_patch ? 'is_superuser' then (p_patch->>'is_superuser')::boolean else a.is_superuser end,
      updated_at = now()
  where a.id = p_target_user_id
  returning to_jsonb(a.*) into v_result;

  if v_result is null then
    raise exception 'PWF_ADMIN_PROFILE_NOT_FOUND' using errcode = 'P0001';
  end if;

  perform platform._pwf_owner_write_audit_v1('core_admin_user_profile_update', v_actor, 'core', 'admin_users', p_target_user_id, null, p_patch, v_result);
  return jsonb_build_object('success', true, 'message_ar', 'تم تحديث الملف الإداري عبر RPC محكوم.', 'data', v_result);
end;
$$;

create or replace function public.rpc_core_admin_user_link_v1(
  p_target_user_id uuid,
  p_patch jsonb default '{}'::jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, core, platform, auth
as $$
declare
  v_ctx jsonb;
  v_actor uuid;
  v_auth_email text;
  v_payload jsonb;
  v_result jsonb;
begin
  if p_target_user_id is null then
    raise exception 'PWF_TARGET_USER_REQUIRED' using errcode = 'P0001';
  end if;
  p_patch := coalesce(p_patch, '{}'::jsonb);
  v_ctx := platform._pwf_owner_write_actor_context_v1();
  v_actor := (v_ctx->>'actor_user_id')::uuid;
  if not (coalesce((v_ctx->>'is_superuser')::boolean,false) or coalesce((v_ctx->>'is_platform_admin')::boolean,false)) then
    raise exception 'PWF_ADMIN_LINK_REQUIRES_PLATFORM_ADMIN' using errcode = 'P0001';
  end if;

  select email into v_auth_email from auth.users where id = p_target_user_id;
  if v_auth_email is null then
    raise exception 'PWF_AUTH_IDENTITY_NOT_FOUND_NO_AUTH_USERS_MUTATION' using errcode = 'P0001';
  end if;

  v_payload := jsonb_build_object(
    'id', p_target_user_id,
    'email', coalesce(nullif(p_patch->>'email',''), v_auth_email),
    'name', coalesce(nullif(p_patch->>'name',''), nullif(p_patch->>'username',''), v_auth_email),
    'role', coalesce(nullif(p_patch->>'role',''), 'user'),
    'is_active', coalesce((p_patch->>'is_active')::boolean, true),
    'updated_at', now()
  ) || (p_patch - 'id' - 'password' - 'auth_user' - 'auth.users');

  if not coalesce((v_ctx->>'is_superuser')::boolean,false) and coalesce((v_payload->>'is_superuser')::boolean,false) then
    raise exception 'PWF_SUPERUSER_GRANT_REQUIRES_SUPERUSER' using errcode = 'P0001';
  end if;

  v_result := platform._pwf_owner_write_upsert_jsonb_v1('core','admin_users', array['id'], v_payload);
  perform platform._pwf_owner_write_audit_v1('core_admin_user_link', v_actor, 'core', 'admin_users', p_target_user_id, null, v_payload, v_result);
  return jsonb_build_object('success', true, 'message_ar', 'تم ربط هوية المصادقة ببروفايل إداري دون تعديل auth.users.', 'data', v_result);
end;
$$;

