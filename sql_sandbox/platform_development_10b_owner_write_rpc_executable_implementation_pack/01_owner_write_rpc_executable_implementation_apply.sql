create or replace function public.rpc_platform_system_register_v1(
  p_system_key text,
  p_payload jsonb default '{}'::jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, platform, auth
as $$
declare
  v_ctx jsonb;
  v_actor uuid;
  v_key_col text;
  v_payload jsonb;
  v_result jsonb;
begin
  p_system_key := btrim(coalesce(p_system_key,''));
  if p_system_key = '' or p_system_key !~ '^[A-Za-z0-9][A-Za-z0-9_\-]{1,80}$' then
    raise exception 'PWF_INVALID_SYSTEM_KEY' using errcode = 'P0001';
  end if;
  v_ctx := platform._pwf_owner_write_actor_context_v1();
  v_actor := (v_ctx->>'actor_user_id')::uuid;
  if not (coalesce((v_ctx->>'is_superuser')::boolean,false) or coalesce((v_ctx->>'is_platform_admin')::boolean,false)) then
    raise exception 'PWF_SYSTEM_REGISTER_REQUIRES_PLATFORM_ADMIN' using errcode = 'P0001';
  end if;
  if platform._pwf_column_exists_v1('platform','platform_systems','system_key') then
    v_key_col := 'system_key';
  elsif platform._pwf_column_exists_v1('platform','platform_systems','key') then
    v_key_col := 'key';
  elsif platform._pwf_column_exists_v1('platform','platform_systems','slug') then
    v_key_col := 'slug';
  else
    raise exception 'PWF_PLATFORM_SYSTEMS_KEY_COLUMN_MISSING' using errcode = 'P0001';
  end if;
  v_payload := coalesce(p_payload,'{}'::jsonb) || jsonb_build_object(v_key_col, p_system_key, 'updated_at', now());
  v_result := platform._pwf_owner_write_upsert_jsonb_v1('platform','platform_systems', array[v_key_col], v_payload);
  perform platform._pwf_owner_write_audit_v1('platform_system_register', v_actor, 'platform', 'platform_systems', null, p_system_key, v_payload, v_result);
  return jsonb_build_object('success', true, 'message_ar', 'تم تسجيل/تحديث النظام عبر RPC محكوم.', 'data', v_result);
end;
