create or replace function platform._pwf_table_exists_v1(p_schema text, p_table text)
returns boolean
language sql
stable
security definer
set search_path = pg_catalog
as $$
  select exists (
    select 1
    from pg_catalog.pg_class c
    join pg_catalog.pg_namespace n on n.oid = c.relnamespace
    where n.nspname = p_schema
      and c.relname = p_table
      and c.relkind in ('r','p','v','m')
  );
$$;

create or replace function platform._pwf_column_exists_v1(p_schema text, p_table text, p_column text)
returns boolean
language sql
stable
security definer
set search_path = pg_catalog
as $$
  select exists (
    select 1
    from pg_catalog.pg_attribute a
    join pg_catalog.pg_class c on c.oid = a.attrelid
    join pg_catalog.pg_namespace n on n.oid = c.relnamespace
    where n.nspname = p_schema
      and c.relname = p_table
      and a.attname = p_column
      and a.attnum > 0
      and not a.attisdropped
  );
$$;

create or replace function platform._pwf_json_cast_expr_v1(p_json_param text, p_column text, p_type text)
returns text
language plpgsql
stable
security definer
set search_path = pg_catalog
as $$
begin
  if p_type in ('json','jsonb') then
    return format('(%s->%L)::%s', p_json_param, p_column, p_type);
  elsif p_type in ('text','character varying','character') or p_type like 'character varying%' or p_type like 'character%' then
    return format('%s->>%L', p_json_param, p_column);
  else
    return format('nullif(%s->>%L, '''')::%s', p_json_param, p_column, p_type);
  end if;
end;
$$;

create or replace function platform._pwf_owner_write_upsert_jsonb_v1(
  p_schema text,
  p_table text,
  p_key_cols text[],
  p_payload jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, platform
as $$
declare
  v_col record;
  v_cols text[] := array[]::text[];
  v_insert_cols text := '';
  v_insert_vals text := '';
  v_update_sets text := '';
  v_where text := '';
  v_sql text;
  v_result jsonb;
  v_expr text;
  v_has_non_key boolean := false;
  v_key text;
begin
  if not platform._pwf_table_exists_v1(p_schema, p_table) then
    raise exception 'PWF_TARGET_TABLE_MISSING %.%', p_schema, p_table using errcode = 'P0001';
  end if;
  if p_payload is null or jsonb_typeof(p_payload) <> 'object' then
    raise exception 'PWF_PAYLOAD_OBJECT_REQUIRED' using errcode = 'P0001';
  end if;
  foreach v_key in array p_key_cols loop
    if not platform._pwf_column_exists_v1(p_schema, p_table, v_key) then
      raise exception 'PWF_TARGET_KEY_COLUMN_MISSING %.%.%', p_schema, p_table, v_key using errcode = 'P0001';
    end if;
    if not (p_payload ? v_key) then
      raise exception 'PWF_TARGET_KEY_VALUE_MISSING %', v_key using errcode = 'P0001';
    end if;
  end loop;

  for v_col in
    select a.attname, pg_catalog.format_type(a.atttypid, a.atttypmod) as type_name
    from pg_catalog.pg_attribute a
    join pg_catalog.pg_class c on c.oid = a.attrelid
    join pg_catalog.pg_namespace n on n.oid = c.relnamespace
    where n.nspname = p_schema
      and c.relname = p_table
      and a.attnum > 0
      and not a.attisdropped
      and (p_payload ? a.attname)
    order by a.attnum
  loop
    v_cols := array_append(v_cols, v_col.attname);
    v_expr := platform._pwf_json_cast_expr_v1('$1', v_col.attname, v_col.type_name);
    v_insert_cols := concat_ws(', ', nullif(v_insert_cols,''), format('%I', v_col.attname));
    v_insert_vals := concat_ws(', ', nullif(v_insert_vals,''), v_expr);
    if not (v_col.attname = any(p_key_cols)) and v_col.attname <> 'created_at' then
      v_has_non_key := true;
      v_update_sets := concat_ws(', ', nullif(v_update_sets,''), format('%I = %s', v_col.attname, v_expr));
    end if;
  end loop;

  if array_length(v_cols, 1) is null then
    raise exception 'PWF_NO_MATCHING_COLUMNS_IN_PAYLOAD %.%', p_schema, p_table using errcode = 'P0001';
  end if;

  foreach v_key in array p_key_cols loop
    select pg_catalog.format_type(a.atttypid, a.atttypmod)
      into v_expr
    from pg_catalog.pg_attribute a
    join pg_catalog.pg_class c on c.oid = a.attrelid
    join pg_catalog.pg_namespace n on n.oid = c.relnamespace
    where n.nspname = p_schema and c.relname = p_table and a.attname = v_key and a.attnum > 0 and not a.attisdropped;
    v_where := concat_ws(' and ', nullif(v_where,''), format('t.%I = %s', v_key, platform._pwf_json_cast_expr_v1('$1', v_key, v_expr)));
  end loop;

  if v_has_non_key then
    v_sql := format('update %I.%I as t set %s where %s returning to_jsonb(t.*)', p_schema, p_table, v_update_sets, v_where);
    execute v_sql using p_payload into v_result;
    if v_result is not null then
      return v_result;
    end if;
  else
    v_sql := format('select to_jsonb(t.*) from %I.%I as t where %s limit 1', p_schema, p_table, v_where);
    execute v_sql using p_payload into v_result;
    if v_result is not null then
      return v_result;
    end if;
  end if;

  v_sql := format('insert into %I.%I as t (%s) values (%s) returning to_jsonb(t.*)', p_schema, p_table, v_insert_cols, v_insert_vals);
  execute v_sql using p_payload into v_result;
  return v_result;
end;
$$;

create or replace function platform._pwf_owner_write_actor_context_v1()
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, core, platform, auth
as $$
declare
  v_actor uuid := auth.uid();
  v_active boolean;
  v_super boolean;
  v_role text;
  v_platform_admin boolean := false;
  v_role_col text;
  v_active_clause text := '';
  v_sql text;
begin
  if v_actor is null then
    raise exception 'PWF_AUTH_REQUIRED' using errcode = 'P0001';
  end if;
  if not platform._pwf_table_exists_v1('core','admin_users') then
    raise exception 'PWF_CORE_ADMIN_USERS_MISSING' using errcode = 'P0001';
  end if;

  execute 'select coalesce(is_active,true), coalesce(is_superuser,false), coalesce(role,'''') from core.admin_users where id = $1'
    into v_active, v_super, v_role using v_actor;

  if v_active is distinct from true then
    raise exception 'PWF_ACTOR_INACTIVE' using errcode = 'P0001';
  end if;

  v_platform_admin := coalesce(v_super,false) or lower(coalesce(v_role,'')) in
    ('superuser','super_user','super-admin','platform_admin','platform-admin','platform_super_admin','root','owner','admin');

  if platform._pwf_table_exists_v1('platform','user_system_roles') then
    if platform._pwf_column_exists_v1('platform','user_system_roles','role_key') then
      v_role_col := 'role_key';
    elsif platform._pwf_column_exists_v1('platform','user_system_roles','role') then
      v_role_col := 'role';
    else
      v_role_col := null;
    end if;
    if platform._pwf_column_exists_v1('platform','user_system_roles','is_active') then
      v_active_clause := ' and coalesce(is_active,true) = true';
    else
      v_active_clause := '';
    end if;
    if v_role_col is not null and platform._pwf_column_exists_v1('platform','user_system_roles','system_key') then
      v_sql := format(
        'select exists(select 1 from platform.user_system_roles where user_id=$1 and lower(system_key::text) in (''platformadmin'',''platform_admin'',''platform-admin'',''platform'',''admin'') and lower(%I::text) in (''superuser'',''super_user'',''platform_admin'',''platform-admin'',''admin'') %s)',
        v_role_col,
        v_active_clause
      );
      execute v_sql into v_platform_admin using v_actor;
      v_platform_admin := v_platform_admin or coalesce(v_super,false);
    end if;
  end if;

  return jsonb_build_object(
    'actor_user_id', v_actor,
    'is_active', coalesce(v_active,false),
    'is_superuser', coalesce(v_super,false),
    'role', coalesce(v_role,''),
    'is_platform_admin', coalesce(v_platform_admin,false)
  );
end;
$$;

create or replace function platform._pwf_owner_write_can_manage_system_v1(p_context jsonb, p_system_key text)
returns boolean
language plpgsql
security definer
set search_path = pg_catalog, platform
as $$
declare
  v_actor uuid := (p_context->>'actor_user_id')::uuid;
  v_role_col text;
  v_active_clause text := '';
  v_sql text;
  v_allowed boolean := false;
begin
  if coalesce((p_context->>'is_superuser')::boolean,false) or coalesce((p_context->>'is_platform_admin')::boolean,false) then
    return true;
  end if;
  if p_system_key is null or btrim(p_system_key) = '' then
    return false;
  end if;
  if not platform._pwf_table_exists_v1('platform','user_system_roles') then
    return false;
  end if;
  if platform._pwf_column_exists_v1('platform','user_system_roles','role_key') then
    v_role_col := 'role_key';
  elsif platform._pwf_column_exists_v1('platform','user_system_roles','role') then
    v_role_col := 'role';
  else
    return false;
  end if;
  if platform._pwf_column_exists_v1('platform','user_system_roles','is_active') then
    v_active_clause := ' and coalesce(is_active,true) = true';
  end if;
  v_sql := format('select exists(select 1 from platform.user_system_roles where user_id=$1 and system_key::text=$2 and lower(%I::text) in (''admin'',''manager'',''unit_admin'',''system_admin'') %s)', v_role_col, v_active_clause);
  execute v_sql into v_allowed using v_actor, p_system_key;
  return coalesce(v_allowed,false);
end;
$$;

